from sklearn.preprocessing import label_binarize
from sklearn.metrics import average_precision_score, classification_report, confusion_matrix

def compute_map(model, loader, num_classes, idx_to_class):
    model.eval()
    all_probs, all_labels = [], []
    with torch.no_grad():
        for images, labels in tqdm(loader, desc='Computing mAP'):
            images = images.to(DEVICE)
            probs  = torch.softmax(model(images), dim=1)
            all_probs.append(probs.cpu().numpy())
            all_labels.append(labels.numpy())

    all_probs  = np.concatenate(all_probs,  axis=0)
    all_labels = np.concatenate(all_labels, axis=0)
    labels_bin = label_binarize(all_labels, classes=list(range(num_classes)))

    per_class_ap = {}
    for c in range(num_classes):
        if labels_bin[:, c].sum() == 0:
            continue
        ap = average_precision_score(labels_bin[:, c], all_probs[:, c])
        per_class_ap[idx_to_class[c]] = ap

    return np.mean(list(per_class_ap.values())), per_class_ap

