class AVADataset(Dataset):
    def __init__(self, csv_path, transform=None, max_samples_per_class=None):
        df = pd.read_csv(csv_path)
        df = df[df['path'].apply(os.path.exists)].reset_index(drop=True)

        # Subsample per class to keep dataset small and balanced
        if max_samples_per_class:
            df = (df.groupby('label')
                    .apply(lambda x: x.sample(min(len(x), max_samples_per_class),
                                               random_state=42))
                    .reset_index(drop=True))

        self.df        = df
        self.transform = transform
        print(f'  Loaded {len(self.df)} samples ({csv_path.split("/")[-1]})')

    def __len__(self):
        return len(self.df)

    def __getitem__(self, idx):
        row   = self.df.iloc[idx]
        image = Image.open(row['path']).convert('RGB')
        if self.transform:
            image = self.transform(image)
        return image, int(row['label'])

