class BaselineCNN(nn.Module):
    """
    A from-scratch CNN with 4 convolutional blocks.
    Deliberately simple — this is our baseline to beat with EfficientNet and SlowFast.

    Architecture:
      Block 1: Conv(3→32)   → BN → ReLU → MaxPool
      Block 2: Conv(32→64)  → BN → ReLU → MaxPool
      Block 3: Conv(64→128) → BN → ReLU → MaxPool
      Block 4: Conv(128→256)→ BN → ReLU → AdaptiveAvgPool
      Head:    FC(256→512) → Dropout → FC(512→num_classes)
    """
    def __init__(self, num_classes):
        super().__init__()

        def conv_block(in_ch, out_ch):
            return nn.Sequential(
                nn.Conv2d(in_ch, out_ch, kernel_size=3, padding=1, bias=False),
                nn.BatchNorm2d(out_ch),
                nn.ReLU(inplace=True),
                nn.Conv2d(out_ch, out_ch, kernel_size=3, padding=1, bias=False),
                nn.BatchNorm2d(out_ch),
                nn.ReLU(inplace=True),
                nn.MaxPool2d(2, 2)
            )

        self.features = nn.Sequential(
            conv_block(3,   32),   # 224 → 112
            conv_block(32,  64),   # 112 → 56
            conv_block(64,  128),  # 56  → 28
            conv_block(128, 256),  # 28  → 14
            nn.AdaptiveAvgPool2d((4, 4))  # → 256×4×4 = 4096
        )

        self.classifier = nn.Sequential(
            nn.Flatten(),
            nn.Linear(256 * 4 * 4, 512),
            nn.ReLU(inplace=True),
            nn.Dropout(0.5),
            nn.Linear(512, num_classes)
        )

    def forward(self, x):
        return self.classifier(self.features(x))


