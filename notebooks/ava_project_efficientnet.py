class EfficientNetAVA(nn.Module):
    """
    EfficientNet-B2 pretrained on ImageNet, fine-tuned for AVA action classification.

    Strategy:
      - Load EfficientNet-B2 with ImageNet weights (via timm)
      - Freeze early layers (stem + blocks 0-3) — keep low-level features
      - Fine-tune blocks 4-6 + head — adapt to action recognition
      - Replace classifier head with a custom 2-layer head + dropout

    Why B2 over B0?
      - B2 has stronger features than B0 but is lighter than B4/B7
      - Good balance of accuracy vs GPU memory for Colab T4
    """
    def __init__(self, num_classes, dropout=0.4):
        super().__init__()

        # Load pretrained EfficientNet-B2
        self.backbone = timm.create_model('efficientnet_b2', pretrained=True)

        # ── Freeze stem + first 4 blocks ──────────────────────────
        # We keep ImageNet low-level features frozen (edges, textures)
        # and only fine-tune the deeper, more semantic layers
        layers_to_freeze = [self.backbone.conv_stem,
                            self.backbone.bn1,
                            *list(self.backbone.blocks)[:4]]
        for layer in layers_to_freeze:
            for param in layer.parameters():
                param.requires_grad = False

        # ── Replace classifier head ────────────────────────────────
        in_features = self.backbone.classifier.in_features
        self.backbone.classifier = nn.Sequential(
            nn.Linear(in_features, 512),
            nn.ReLU(inplace=True),
            nn.Dropout(dropout),
            nn.Linear(512, num_classes)
        )

    def forward(self, x):
        return self.backbone(x)


