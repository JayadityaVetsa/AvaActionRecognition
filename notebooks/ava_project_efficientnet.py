LR           = 3e-4
WEIGHT_DECAY = 1e-4

# Class weights for imbalance
train_df     = pd.read_csv(TRAIN_CSV)
class_counts = train_df['label'].value_counts().sort_index().values
class_weights = torch.tensor(1.0 / class_counts, dtype=torch.float32).to(DEVICE)
class_weights = class_weights / class_weights.sum() * NUM_CLASSES

criterion = nn.CrossEntropyLoss(weight=class_weights)

# Two param groups: lower LR for unfrozen backbone, higher for new head
backbone_params = [p for name, p in model.named_parameters()
                   if p.requires_grad and 'classifier' not in name]
head_params     = [p for name, p in model.named_parameters()
                   if p.requires_grad and 'classifier' in name]

optimizer = optim.AdamW([
    {'params': backbone_params, 'lr': LR * 0.1},   # 3e-5 for backbone
    {'params': head_params,     'lr': LR}           # 3e-4 for head
], weight_decay=WEIGHT_DECAY)

# Warm up for 2 epochs then cosine decay
scheduler = optim.lr_scheduler.OneCycleLR(
    optimizer,
    max_lr=[LR * 0.1, LR],
    steps_per_epoch=len(train_loader),
    epochs=EPOCHS,
    pct_start=0.2   # 20% warmup
)

print(f'✅ Loss: CrossEntropyLoss (class-weighted)')
