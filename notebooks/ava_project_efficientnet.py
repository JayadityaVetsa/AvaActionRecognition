for epoch in range(1, EPOCHS + 1):

    # ── Train ──────────────────────────────────────────────────
    model.train()
    total_loss, correct, total = 0.0, 0, 0

    for i, (images, labels) in enumerate(train_loader):
        images, labels = images.to(DEVICE), labels.to(DEVICE)
        optimizer.zero_grad()
        outputs = model(images)
        loss    = criterion(outputs, labels)
        loss.backward()
        optimizer.step()
        scheduler.step()

        total_loss += loss.item() * images.size(0)
        correct    += (outputs.argmax(1) == labels).sum().item()
        total      += images.size(0)

        if (i + 1) % 20 == 0:
            print(f'  Epoch {epoch} | batch {i+1}/{len(train_loader)} '
                  f'| loss={total_loss/total:.4f} | acc={correct/total:.3f}', flush=True)

    train_loss = total_loss / total
    train_acc  = correct / total

    # ── Validate ───────────────────────────────────────────────
    model.eval()
    v_loss, v_correct, v_total = 0.0, 0, 0
    with torch.no_grad():
        for images, labels in val_loader:
            images, labels = images.to(DEVICE), labels.to(DEVICE)
            outputs = model(images)
            v_loss    += criterion(outputs, labels).item() * images.size(0)
            v_correct += (outputs.argmax(1) == labels).sum().item()
            v_total   += images.size(0)

    val_loss = v_loss / v_total
    val_acc  = v_correct / v_total

    history['train_loss'].append(train_loss)
    history['train_acc'].append(train_acc)
    history['val_loss'].append(val_loss)
    history['val_acc'].append(val_acc)

    if val_acc > best_val_acc:
        best_val_acc = val_acc
        torch.save(model.state_dict(), best_model_path)
        flag = '  ← best'
    else:
        flag = ''

    print(f'\nEpoch {epoch:2d}/{EPOCHS}  '
          f'train_loss={train_loss:.4f}  train_acc={train_acc:.3f}  '
          f'val_loss={val_loss:.4f}  val_acc={val_acc:.3f}{flag}\n', flush=True)

