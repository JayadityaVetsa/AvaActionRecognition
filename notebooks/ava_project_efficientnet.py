def full_evaluation(model, loader, num_classes, idx_to_class):
    model.eval()
    all_probs, all_preds, all_labels = [], [], []

    with torch.no_grad():
        for images, labels in tqdm(loader, desc='Evaluating'):
            images  = images.to(DEVICE)
            outputs = model(images)
            probs   = torch.softmax(outputs, dim=1)
            all_probs.append(probs.cpu().numpy())
            all_preds.extend(outputs.argmax(1).cpu().numpy())
            all_labels.extend(labels.numpy())

    all_probs  = np.concatenate(all_probs, axis=0)
    all_preds  = np.array(all_preds)
    all_labels = np.array(all_labels)

    # ── Accuracy ───────────────────────────────────────────────
    accuracy = (all_preds == all_labels).mean()

    # ── mAP ────────────────────────────────────────────────────
    labels_bin   = label_binarize(all_labels, classes=list(range(num_classes)))
    per_class_ap = {}
    for c in range(num_classes):
        if labels_bin[:, c].sum() == 0:
            continue
        ap = average_precision_score(labels_bin[:, c], all_probs[:, c])
        per_class_ap[idx_to_class[c]] = ap
    mAP = np.mean(list(per_class_ap.values()))

    return accuracy, mAP, per_class_ap, all_preds, all_labels


