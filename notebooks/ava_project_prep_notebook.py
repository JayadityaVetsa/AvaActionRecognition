def select_diverse_videos(df, n_videos, min_annotations=100):
    """
    Greedy set-cover: pick videos that together cover the most unique action classes.
    Prefers videos with more annotations.
    """
    video_stats = (
        df.groupby('video_id')
          .agg(
              annotation_count=('action_id', 'count'),
              class_set=('action_id', lambda x: frozenset(x))
          )
          .reset_index()
    )
    video_stats = video_stats[video_stats['annotation_count'] >= min_annotations]

    selected      = []
    covered       = set()

    for _ in range(n_videos):
        best_vid, best_new, best_set = None, -1, set()
        for _, row in video_stats.iterrows():
            if row['video_id'] in selected:
                continue
            new = len(row['class_set'] - covered)
            if new > best_new:
                best_new, best_vid, best_set = new, row['video_id'], row['class_set']
        if best_vid is None:
            break
        selected.append(best_vid)
        covered |= best_set

    print(f'Selected {len(selected)} videos covering {len(covered)} unique classes')
    return selected


