def parse_label_map(pbtxt_path):
    label_map = {}
    with open(pbtxt_path, 'r') as f:
        content = f.read()
    entries = re.findall(r'label\s*\{([^}]+)\}', content, re.DOTALL)
    for entry in entries:
        name_match = re.search(r'name:\s*"([^"]+)"', entry)
        id_match   = re.search(r'label_id:\s*(\d+)', entry)
        if name_match and id_match:
            label_map[int(id_match.group(1))] = name_match.group(1)
    return label_map

