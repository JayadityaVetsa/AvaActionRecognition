def download_video(video_id, output_dir):
    url = f'https://www.youtube.com/watch?v={video_id}'
    out = os.path.join(output_dir, f'{video_id}.mp4')

    if os.path.exists(out):
        print(f'  ⏭️  {video_id}: already downloaded')
        return out

    cmd = [
        'yt-dlp',
        '--format', 'bestvideo[height<=360][ext=mp4]+bestaudio[ext=m4a]/best[height<=360]',
        '--merge-output-format', 'mp4',
        '--output', out,
        '--no-playlist', '--quiet', '--no-warnings',
        url
    ]
    try:
        result = subprocess.run(cmd, capture_output=True, text=True, timeout=300)
        if result.returncode == 0 and os.path.exists(out):
            size_mb = os.path.getsize(out) / (1024**2)
            print(f'  ✅ {video_id}: {size_mb:.1f} MB')
            return out
        print(f'  ❌ {video_id}: failed — {result.stderr[:120]}')
        return None
    except Exception as e:
        print(f'  ❌ {video_id}: {e}')
        return None


def download_until_target(all_candidates, output_dir, target, label):
    """
    Try candidates in order, keep going until we have `target` successful downloads.
    `all_candidates` is the full ranked list from select_diverse_videos.
    """
    downloaded = {}
    print(f'=== Downloading {label} videos (need {target}) ===')
    for vid in all_candidates:
        if len(downloaded) >= target:
            break
        path = download_video(vid, output_dir)
        if path:
            downloaded[vid] = path
    print(f'  → Got {len(downloaded)}/{target}\n')
    return downloaded


