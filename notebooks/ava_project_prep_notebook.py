CROP_SIZE = 224
PADDING   = 0.10

def crop_and_save(frame_path, row, output_path):
    if os.path.exists(output_path):
        return True
    img = cv2.imread(frame_path)
    if img is None:
        return False
    H, W = img.shape[:2]
    x1 = max(0, int((row['x1'] - PADDING) * W))
    y1 = max(0, int((row['y1'] - PADDING) * H))
    x2 = min(W, int((row['x2'] + PADDING) * W))
    y2 = min(H, int((row['y2'] + PADDING) * H))
    if x2 - x1 < 10 or y2 - y1 < 10:
        return False
    crop = cv2.resize(img[y1:y2, x1:x2], (CROP_SIZE, CROP_SIZE), interpolation=cv2.INTER_LANCZOS4)
    cv2.imwrite(output_path, crop)
    return True


def build_crops(df_sub, split_name):
    manifest = []
    for _, row in tqdm(df_sub.iterrows(), total=len(df_sub), desc=f'Cropping {split_name}'):
        vid, ts = row['video_id'], row['timestamp']
        if vid not in frame_map or ts not in frame_map[vid]:
            continue
        cls_folder = row['action_name'].replace('/', '_').replace(' ', '_').replace('(','').replace(')','')
        cls_dir    = os.path.join(CROPS_DIR, split_name, cls_folder)
        os.makedirs(cls_dir, exist_ok=True)
        fname      = f"{vid}_{int(ts):04d}_{int(row['person_id'])}.jpg"
        out_path   = os.path.join(cls_dir, fname)
        if crop_and_save(frame_map[vid][ts], row, out_path):
            manifest.append({
                'path': out_path, 'label': int(row['label']),
                'class_name': row['action_name'], 'video_id': vid,
                'timestamp': int(ts), 'person_id': int(row['person_id'])
            })
    return manifest


